package in.cdacnoida.dava.model;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "SEC_LIST")
@XmlAccessorType (XmlAccessType.FIELD)
public class SEC_LIST {

	@XmlElement(name = "SECONDARY")
	private List<SECONDARY> secondary=new ArrayList<SECONDARY>();

	public List<SECONDARY> getSecondary() {
		return secondary;
	}

	public void addSecondary(SECONDARY secondary) {
		this.secondary.add(secondary);
	}
	
	
	
	
}
