public class File {

	private String encodedFile;

	public File() {
	}

	public String getEncodedFile() {
		return encodedFile;
	}

	public void setEncodedFile(String encodedFile) {
		this.encodedFile = encodedFile;
	}

	@Override
	public String toString() {
		return "SendFile [encodedFile=" + encodedFile + "]";
	}
}
