package in.cdacnoida.dava.model;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "List_srno")
@XmlAccessorType (XmlAccessType.FIELD)
public class List_srno {

	public List_srno() {}
	
	@XmlElement(name = "srno")
	private List<String> srNo=new ArrayList<String>();

	public List<String> getSrNo() {
		return srNo;
	}

	public void addSrNo(String srNo) {
		this.srNo.add(srNo);
	}

}
